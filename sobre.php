			<h1>Redes de Computadores & Sistemas Distribuídos</h1>
			
			<p>
			<small>Última modificação: 13/10/2013</small>
			<br>
			A disciplina de <b>Redes de Computadores & Sistemas Distribuídos</b> do curso de <b>Engenharia de Software</b> da <b>Universidade Federal do Ceará</b> visa dar subsídio para que o aluno compreenda os conceitos básicos de redes de computadores e sistemas distribuídos, seus componentes, funcionamento e gerenciamento para ajudá-lo a ter uma visão mais aprofundada no desenvolvimento de sistemas com qualidade na utilização dos recursos computacionais.</p>
			
			<br><h3>Sobre este Trabalho:</h3>
			<p>Nosso projeto consiste em utilizar da tecnologia de websocket (comunicação bidirecional por canais full-duplex sobre um único soquete do Transmission Control Protocol) para promover a experiência de ensino através da simulação de um ambiente escolar, que consiste em um quadro negro para ser utilizado de maneira colaborativa.</p>
			
